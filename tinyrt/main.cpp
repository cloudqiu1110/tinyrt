Intersect getFirstIntersection(Ray* ray, Object *objs[], int num)
{
    Intersect intersection = {0,0,0, -1};

    for (int i = 0; i < num; i++)
    {
        Object* obj = objs[i];
        switch (obj->type)
        {
        case SPHERE:
            intersection = intersectSphere(ray, (Sphere*)obj->o );
        default:
            break;
        }
    }


    return intersection;
}


